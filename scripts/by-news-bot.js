function request(method, urlPath, body, extraHeaders = {}) {
    return new Promise((resolve, reject) => {
        const urlObj = new URL(WP_URL + urlPath);
        const proto = urlObj.protocol === 'https:' ? https : http;
        const options = {
            hostname: urlObj.hostname, path: urlObj.pathname + urlObj.search,
            method, headers: { 'Authorization': `Basic ${AUTH}`, 'Content-Type': 'application/json', ...extraHeaders }
        };
        const req = proto.request(options, res => {
            let data = '';
            res.on('data', c => data += c);
            res.on('end', () => { try { resolve(JSON.parse(data)); } catch { resolve({ raw: data, status: res.statusCode }); } });
        });
        req.on('error', reject);
        if (body) req.write(JSON.stringify(body));
        req.end();
    });
}

function downloadFile(url, dest) {
    return new Promise((resolve, reject) => {
        const urlObj = new URL(url);
        const proto = urlObj.protocol === 'https:' ? https : http;
        const file = fs.createWriteStream(dest);
        proto.get(url, { headers: { 'User-Agent': 'Mozilla/5.0' } }, res => {
            if (res.statusCode >= 300 && res.headers.location) { file.close(); return downloadFile(res.headers.location, dest).then(resolve).catch(reject); }
            res.pipe(file);
            file.on('finish', () => { file.close(); resolve(); });
        }).on('error', reject);
    });
}

async function uploadMedia(filePath, fileName, altText) {
    const boundary = '----B' + Math.random().toString(36).slice(2);
    const fileData = fs.readFileSync(filePath);
    const header = Buffer.from(`--${boundary}\r\nContent-Disposition: form-data; name="file"; filename="${fileName}"\r\nContent-Type: image/jpeg\r\n\r\n`);
    const footer = Buffer.from(`\r\n--${boundary}--\r\n`);
    const bodyBuffer = Buffer.concat([header, fileData, footer]);
    return new Promise(resolve => {
        const urlObj = new URL(WP_URL + '/wp-json/wp/v2/media');
        const req = https.request({
            hostname: urlObj.hostname, path: urlObj.pathname, method: 'POST',
            headers: { 'Authorization': `Basic ${AUTH}`, 'Content-Type': `multipart/form-data; boundary=${boundary}`, 'Content-Length': bodyBuffer.length }
        }, res => {
            let d = ''; res.on('data', c => d += c);
            res.on('end', () => { try { const j = JSON.parse(d); resolve({ id: j.id || null, url: j.source_url || null }); } catch { resolve({ id: null, url: null }); } });
        });
        req.on('error', () => resolve({ id: null, url: null }));
        req.write(bodyBuffer); req.end();
    });
}

async function createPost(title, content, categoryId, featuredMediaId, yoastMeta) {
    const body = { title, content, status: 'publish', categories: [categoryId] };
    if (featuredMediaId) body.featured_media = featuredMediaId;
    if (yoastMeta) body.meta = yoastMeta;
    return request('POST', '/wp-json/wp/v2/posts', body);
}

async function updatePostMeta(postId, meta) {
    return request('POST', `/wp-json/wp/v2/posts/${postId}`, { meta });
}

